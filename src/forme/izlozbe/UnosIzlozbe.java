    private void srediFormu() {
        cmbSprat.removeAllItems();
        for (int i = 1; i < 4; i++) {
            cmbSprat.addItem(i);

        }
        cmbSala.removeAllItems();
        for (int i = 1; i < 7; i++) {
           cmbSala.addItem(i);
        }
        srediButtonGroup();
        srediTabelu(listaZaTabelu);
        popuniCmbDela();
        srediZaListener();
       
        
    }

    private void srediButtonGroup() {
        
        ButtonGroup bg = new ButtonGroup();
        bg.add(rbStalna);
        bg.add(rbPrivremena);
    }

    private void srediTabelu(List<Delo> lista) {
        ModelTabeleDela mtd = new ModelTabeleDela(lista);
        tblDela.setFont(new Font("Lucida Bright", Font.PLAIN, 14));
        tblDela.getTableHeader().setFont(new Font("Lucida Bright", Font.PLAIN, 14));
        tblDela.setModel(mtd);
    }

    private void popuniCmbDela() {
        ServerskiOdgovor so = Kontroler.getInstanca().vratiListuDela(new Delo(-1, "", "", ""));
        if(so!=null){
        listaDela = (List<Delo>) so.getParametar();
        cmbDela.removeAllItems();
        cmbDela.insertItemAt(null, 0);
        for (Delo delo : listaDela) {
            cmbDela.addItem(delo);
        }
        
        System.out.println("Sistem je vratio listu dela.");
        }
        else {
            JOptionPane.showMessageDialog(this, "Neuspe≈°no uƒçitana lista dela. Poku≈°ajte kasnije");
            btnSacuvajPostavku.setEnabled(false);
            return;
        }
    }

    private void srediZaListener() {
        rbPrivremena.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                datDatumDo.setEnabled(true);
                datDatumOd.setEnabled(true);
            }
        });
        rbStalna.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                datDatumDo.setEnabled(false);
                datDatumOd.setEnabled(false);
            }
        });
    }
