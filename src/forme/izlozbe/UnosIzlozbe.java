    private void btnDodajDeloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajDeloActionPerformed
        Delo delo = (Delo) cmbDela.getSelectedItem();
        int odabranoDelo = cmbDela.getSelectedIndex();
        if(odabranoDelo == 0 || odabranoDelo == -1){
            System.out.println("Izaberi bar jedno");
            JOptionPane.showMessageDialog(this, "Morate da izaberete bar jedno delo.");
        }else{
            if(!listaZaTabelu.contains(delo)){
                listaZaTabelu.add(delo);
                ModelTabeleDela mtd = new ModelTabeleDela(listaZaTabelu);
                tblDela.setModel(mtd);
            }
            else{
                JOptionPane.showMessageDialog(this, "Veƒá ste ubacili izabrano delo.");
            }
        }
    }//GEN-LAST:event_btnDodajDeloActionPerformed

