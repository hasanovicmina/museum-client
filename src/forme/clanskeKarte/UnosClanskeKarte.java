    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String ime = txtIme.getText().trim();
        String poruka = "Članska karta nije kreirana!\n";
        if(ime.isEmpty()){
            String p = "Ime je obavezno polje.";
            JOptionPane.showMessageDialog(this, poruka+p);
            return;
            
        }
     
        String prezime = txtPrezime.getText().trim();
        if(prezime.isEmpty()){
            String p = "Prezime je obavezno polje.";
            JOptionPane.showMessageDialog(this, poruka+p);
            return;
            
        }
        
        String imePrezime = ime+" "+prezime;
        if(imePrezime.length()>=50){
            String p = "Predugačko ime i prezime.";
            JOptionPane.showMessageDialog(this, poruka+p);
            return;
        }
        String kategorija = (String) cmbKategorije.getSelectedItem();
        Date dat = txtDatum.getDate();
        if(dat == null){
            String p = "Datum je obavezno polje.";
            JOptionPane.showMessageDialog(this, poruka+p);
            return;

        }
      
          
            if(dat.before(new Date()) || dat.equals(new Date())){
                String p = "Datum važenja mora biti nakon današnjeg datuma.";
                JOptionPane.showMessageDialog(this, poruka+p);
                return;
            }
            Calendar kal1 = Calendar.getInstance();
            Calendar kal2 = Calendar.getInstance();

            kal1.setTime(dat);
            kal2.setTime(new Date());
            if(kal1.get(Calendar.YEAR) != kal2.get(Calendar.YEAR)+1){
                String p = "Godina važenja mora biti sledeće godine.";
                JOptionPane.showMessageDialog(this, poruka+p);
                return;                
            }
            if(kal2.get(Calendar.MONTH) == 1 && kal2.get(Calendar.DAY_OF_MONTH) == 29 && kal2.get(Calendar.YEAR)% 4 == 0){
                if(kal1.get(Calendar.DAY_OF_MONTH) != 1 && kal1.get(Calendar.MONTH) != 2){
                    String p = "Prestupna je godina. Datum vazenja ne moze biti isti dan i mesec.";
                    JOptionPane.showMessageDialog(this, poruka+p);
                    return;
                }
            }else{
                Zaposleni zaposleni = Sesija.getInstanca().getZaposleni();
                ClanskaKarta ck = new ClanskaKarta(-1, imePrezime, kategorija, dat, zaposleni.getZaposleniID());
                ServerskiOdgovor so = Kontroler.getInstanca().kreirajClanskuKartu(ck);
                
                if(so != null){
                    if(so.isUspesno()){
                        System.out.println(so.getPoruka());
                        JOptionPane.showMessageDialog(this, "Članska karta je uspešno kreirana.");
                    }else{
                         JOptionPane.showMessageDialog(this, "Članska karta nije kreirana.");
                       
                    }
                }
                
            }
                    
        
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

