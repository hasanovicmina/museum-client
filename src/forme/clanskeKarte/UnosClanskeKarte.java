    private void srediFormu() {
        ServerskiOdgovor so = Kontroler.getInstanca().vratiListuKategorija();
        ArrayList<String> listaKategorija = (ArrayList<String>) so.getParametar();
        cmbKategorije.removeAllItems();
        for (String k : listaKategorija) {
            cmbKategorije.addItem(k);
        }
        System.out.println("Sistem je vratio listu posebnih kategorija.");
        txtDatum.setDate(new Date());
        
    }
