    private void btnPretraziDatumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziDatumActionPerformed
         Date datum = datAngazovanja.getDate();
         if(datum == null){
            JOptionPane.showMessageDialog(this, "Morate da izaberete datum!");
            return;
         }
        Vodic vodic = new Vodic(-1, "", datum, -1);
        listaSaKriterijumom = new ArrayList<>();
        ServerskiOdgovor so = Kontroler.getInstanca().nadjiVodice(vodic);
        if (so != null && so.getParametar() != null) {
            ModelTabeleVodici mtv = (ModelTabeleVodici) tblVodici.getModel();
            mtv.isprazniListu();
            listaSaKriterijumom = (List<Vodic>) so.getParametar();
            mtv.setLista(listaSaKriterijumom);
            if (listaSaKriterijumom.size() == 0) {
                JOptionPane.showMessageDialog(this, porukaNeg + "Ne postoje vodiči koji su angažovani tog dana.");
                return;
            } else {
                JOptionPane.showMessageDialog(this, porukaPoz);

            }
        }else{
            JOptionPane.showMessageDialog(this,porukaNeg);
            btnPretraziDatum.setEnabled(false);
            btnPretraziIzlozba.setEnabled(false);
            
        }
         
    }//GEN-LAST:event_btnPretraziDatumActionPerformed

    private void btnPretraziIzlozbaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziIzlozbaActionPerformed
        Izlozba izlozba = (Izlozba) cmbIzlozbe.getSelectedItem();
        if (izlozba == null) {
            JOptionPane.showMessageDialog(this, "Morate prvo da izaberete izlozbu!");
            return;
        }
    
        int izlozbaID = izlozba.getIzlozbaID();
        listaSaKriterijumom = new ArrayList<>();
        Vodic v = new Vodic(-1, "", null, izlozbaID);
        ServerskiOdgovor so = Kontroler.getInstanca().nadjiVodice(v);
        if(so != null && so.getParametar()!=null){
            ModelTabeleVodici mtv = (ModelTabeleVodici) tblVodici.getModel();
            mtv.isprazniListu();
            listaSaKriterijumom = (List<Vodic>) so.getParametar();
            mtv.setLista(listaSaKriterijumom);
            if(listaSaKriterijumom.size() == 0){
                JOptionPane.showMessageDialog(this,porukaNeg+"Ne postoje vodiči koji se nalaze na izabranoj izložbi.");
                return;
            }else{
                JOptionPane.showMessageDialog(this,porukaPoz);

            }
        }else{
            JOptionPane.showMessageDialog(this, porukaNeg);
            btnPretraziDatum.setEnabled(false);
            btnPretraziIzlozba.setEnabled(false);
        }
    }//GEN-LAST:event_btnPretraziIzlozbaActionPerformed

