    private void srediFormu() {
        
        popuniCmbIzlozbe();
        
        listaSvihVodica = new ArrayList<>();
        ModelTabeleVodici mtv = new ModelTabeleVodici();
        tblVodici.setModel(mtv);
        Vodic vodic = new Vodic(-1, "", null, -1);
        ServerskiOdgovor so = Kontroler.getInstanca().vratiListuVodica(vodic);
        listaSvihVodica = (List<Vodic>) so.getParametar();
        if(so!=null && listaSvihVodica!=null){
            mtv.setLista(listaSvihVodica);
        }else{
            JOptionPane.showMessageDialog(this,"Greska u ucitavanju liste");
            btnPretraziDatum.setEnabled(false);
            btnPretraziIzlozba.setEnabled(false);
        }
    }

    private void popuniCmbIzlozbe() {
        List<Izlozba> listaIzlozbi= new ArrayList<>();
        Izlozba izlozba = new Izlozba(-1, "", "", null, null);
        ServerskiOdgovor so = Kontroler.getInstanca().vratiListuIzlozbi(izlozba);
        if (so != null && so.getParametar()!=null) {
            listaIzlozbi = (List<Izlozba>) so.getParametar();
            cmbIzlozbe.removeAllItems();
            cmbIzlozbe.insertItemAt(null, 0);
            for (Izlozba i : listaIzlozbi) {
                cmbIzlozbe.addItem(i);
            }

            System.out.println("Sistem je vratio listu izlozbi.");
        } else {
            JOptionPane.showMessageDialog(this, "Neuspešno učitana lista izlozbi. Pokušajte kasnije");
            return;
        }
    }
